supplies = ['pens', 'staplers', 'flamethrowers', 'binders']

# Goes through each index in the range and outputs the supplies list

for i in range(len(supplies)):
    print('Index ' + str(i) + ' in supplies is: ' + supplies[i])

# For loops technically iterate over the values in a list

# The range() function returns a list-like value, which can be passed to the
# list() function if you need an actual list value
